const typeDefs = gql`
  type Book {
    title: String!
    published: Int!
    author: ID!
    id: ID!
    genres: [String!]
  }

  type Author {
    name: String!
    id: ID!
    born: Int
    books: Int
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addAuthor(name: String!, born: Int, books: Int): Author
    addBook(
      title: String!
      author: ID!
      published: Int!
      genres: [String!]!
    ): Book
    editAuthor(name: String!, setBornTo: Int!): Author
  }
`;

const resolvers = {
  Query: {
    bookCount: async () => await BookModel.countDocuments(),
    authorCount: async () => await AuthorModel.countDocuments(),
    allBooks: async (root, args) => {
      const { author } = args;
      if (author) {
        const books = await BookModel.find({ author });
        return books;
      }
      const books = await BookModel.find();
      return books;
    },
    allAuthors: async () => {
      const books = await AuthorModel.find();
      return books;
    },
  },
  Mutation: {
    addBook: async (root, args) => {
      try {
        const book = new BookModel(args);
        return await book.save();
      } catch (error) {
        throw error;
      }
    },
    editAuthor: async (root, args) => {
      const { name, setBornTo } = args;
      const author = await AuthorModel.findOne({ name });
      author.born = setBornTo;
      const newAuthor = await author.save();
      return newAuthor;
    },
    addAuthor: async (root, args) => {
      const author = new AuthorModel(args);
      try {
        return await author.save();
      } catch (error) {
        console.log(error);
      }
    },
  },
};

const server = new ApolloServer({
  typeDefs,
  resolvers,
});

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`);
});
